
namespace VisualStudioTips
{
    // Put the cursor here at the beginning of the class declaration. Press Ctrl + M, Ctrl + M. See how the class gets collapsed. 
    // Press Ctrl + M, Ctrl + M again to expand the class. 
    public class ExpandCollapse
    {
        // Now put the cursor here and repeat. Note that this time you expand/collapse only the Start() method. 
        public void Start()
        {
            System.Console.WriteLine("Hello, World");

            // Now put the cursor here. Use Ctrl + Home to jump to the beginning of the file. Put the cursor near the namespace declaration
            // and press Ctrl + M and Ctrl + M again to collapse the entire namespace.
            System.Console.WriteLine("Hello, World");
        }


    }
}
